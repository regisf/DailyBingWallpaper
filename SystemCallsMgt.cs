using System.Runtime.InteropServices;

namespace DailyBingWallpaper 
{
    /// <summary>
    /// Handle interaction with the OS
    /// </summary>
    internal class SystemCallsMgt
    {
        const int SPI_SETDESKWALLPAPER = 20;
        const int SPIF_UPDATEINIFILE = 0x01;
        const int SPIF_SENDWININICHANGE = 0x02;

        [DllImport("user32.dll", CharSet = CharSet.Auto)]
        static extern int SystemParametersInfo(int uAction, int uParam, string lpvParam, int fuWinIni);

        /// <summary>
        /// Change the OS registry to set the image as a desktop image
        /// </summary>
        /// <param name="imagePath">The image path</param>
        internal static void SetImageAsWallpaper(string imagePath)
        {
            SystemParametersInfo(SPI_SETDESKWALLPAPER, 0, imagePath, SPIF_UPDATEINIFILE | SPIF_SENDWININICHANGE);
        }

    }
}
